public class LutadorAtributes {
    String nome;
    int idade;
    double peso;
    double altura;

    public void setNome(String nome){
        this.nome = nome;
    }
    public String getNome(){
        return nome;
    }
    public void setIdade(int idade){
        this.idade = idade;

    }
    public int getIdade(){
        return this.idade;
    }

    public void setPeso(double peso){
        this.peso = peso;
    }

    public double getPeso(){
        return  this.peso;
    }

    public void setAltura(double altura){
        this.altura = altura;
    }

    public double getAltura(){
        return this.altura;
    }




}
